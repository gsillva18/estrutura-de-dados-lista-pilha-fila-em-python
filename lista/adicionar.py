listaFrutas = ["Graviola", "Morango", "Banana"]

print("\n","Lista inicial".center(50,"-"),"\n")
print(listaFrutas)

#adicionando frutas utilizando o método append
listaFrutas.append("Manga")
listaFrutas.append("Abacaxi")

print("\n","Lista usando o método APPEND".center(50,"-"),"\n")
print(listaFrutas)

#adicionando frutas utilizando o método insert no índice 1
listaFrutas.insert(1,"Maçã")

print("\n","Lista usando o método INSERT".center(50,"-"),"\n")
print(listaFrutas)

        
